void LCD_send_bit(uint8_t data) {		
	if (data) {
		SBI(LCD_PORT, LCD_PINSDA);
	}
	else CBI(LCD_PORT, LCD_PINSDA);
	
	SBI(LCD_PORT, LCD_PINCLC);
	CBI(LCD_PORT, LCD_PINCLC);
}

void LCD_send_byte(uint8_t data, uint8_t dorc) {	
	CBI(LCD_PORT, LCD_PINCSX);
	LCD_send_bit(dorc);

	for (uint8_t m = 0x80; m; m >>= 1) {
		LCD_send_bit(data & m);
	}

	SBI(LCD_PORT, LCD_PINCSX);
}

void LCD_send_command(uint8_t data) { //7414
	LCD_send_byte(data, 0x00);
}

void LCD_send_data(uint8_t data) {
	LCD_send_byte(data, 0x01);
}

void LCD_draw_char(char code) {
  for (int i = 0; i < 6; i++) {
    LCD_send_data(pgm_read_byte(&font57[(code - 32) * 6 + i]));
  }
}

void LCD_draw_string(const char* text) {
	for (unsigned char i = 0; text[i]; i++) {
		LCD_draw_char(text[i]);
	}
}

void LCD_set_position(uint8_t xpos, uint8_t ypos) {
  LCD_send_command(0xB0 | (ypos &  0x0F));    
  LCD_send_command(0x10 | (xpos >> 0x04));
  LCD_send_command(0x0F & xpos);
}

void LCD_clear_screen() {
	LCD_set_position(0, 0);

	for (int i = 0; i < 96*9; i++) {
		LCD_send_data(0);
	}
}

void LCD_FillRectange(uint8_t data, uint8_t xpos, uint8_t ypos, uint8_t width, uint8_t height) {
  	for (uint8_t y = 0; y < height; y++) {
		LCD_set_position(xpos, ypos + y);
	  	for (uint8_t x = 0; x < width; x++) {
			LCD_send_data(data);
		}
	}
}

void LCD_mode_positive(uint8_t mode) {
  LCD_send_command((mode) ? 0xA6 : 0xA7);
}

void LCD_initialize() {
	// port configuration
	LCD_PORT |= _BV(LCD_PINCSX); // signal up LCD_CSX	
	LCD_DDRS |= _BV(LCD_PINCLC) | _BV(LCD_PINSDA) | _BV(LCD_PINCSX); // data direction
	
  	// internal reset
  	LCD_send_command(0xE2);
	// reset delay
	_delay_ms(20);

  	// thermal compensation on
  	LCD_send_command(0xEB);
  	// supply mode
  	LCD_send_command(0x2F);
	// clear screen
	LCD_send_command(0xA4);
	// positive mode (A6, A7)
	LCD_send_command(0xA6);
	// display on
	LCD_send_command(0xAF);
	// max contrast 0
	LCD_send_command(0x90);
}


void LCD_draw_char_X2(char code, uint8_t xpos, uint8_t ypos) {
  for (int b = 0; b < 2; b++) {    		
		LCD_set_position(xpos, ypos + b);

	  for (int i = 0; i < 6; i++) {
			uint8_t d = (pgm_read_byte(&font57[(code - 32) * 6 + i]) >> (b * 4));						

			uint8_t m = 0x00;
			if (d & 0x01) m |= 0x03;
			if (d & 0x02) m |= 0x0C;
			if (d & 0x04) m |= 0x30;
			if (d & 0x08) m |= 0xC0;

			for (int p = 0; p < 2; p++) {
				LCD_send_data(m);
			}
	  }
  }	
}

void LCD_draw_string_X2(const char* text, uint8_t xpos, uint8_t ypos) {
	for (unsigned char i = 0; text[i]; i++) {
		LCD_draw_char_X2(text[i], xpos + i * 12, ypos);
	}
}

void LCD_draw_char_4X(char code, uint8_t xpos, uint8_t ypos) {
  	for (int b = 0; b < 4; b++) {    		
		LCD_set_position(xpos, ypos + b);

	  	for (int i = 0; i < 6; i++) {
			uint8_t d = (pgm_read_byte(&font57[(code - 32) * 6 + i]) >> (b * 2));						

			uint8_t m = 0x00;
			if (d & 0x01) m |= 0x0F;
			if (d & 0x02) m |= 0xF0;

			for (int p = 0; p < 3; p++) {
				LCD_send_data(m);
			}
	  	}
  	}
}

void LCD_draw_string_4X(const char* text, uint8_t xpos, uint8_t ypos) {
	for (unsigned char i = 0; text[i]; i++) {
		LCD_draw_char_4X(text[i], xpos + i * 18, ypos);
	}
}
